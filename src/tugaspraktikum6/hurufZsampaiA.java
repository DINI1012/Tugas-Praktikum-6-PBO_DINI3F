public class hurufZsampaiA {
    public void cetakhurufZsampaiA() {
        System.out.println("Huruf dari Z ke A:");
        for (char ch = 'Z'; ch >= 'A'; ch--) {
            System.out.print(ch + " ");
        }
        System.out.println();
    }
}

